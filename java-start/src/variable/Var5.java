package variable;

public class Var5 {
    public static void main(String[] args) {
        //1. 변수선언 후 초기화는 따로
        int a;
        a = 1; //초기화 한것
        System.out.println(a);

        //한번에 가능
        int b = 2; //변수의 선언과 초기화를 한번에 하는 것
        System.out.println(b);
        int c = 3, d = 4; // 여러 변수의 선언과 초기화가 가능
        System.out.println(c);
        System.out.println(d);
    }
}
